#include <stdio.h> // This is a processing directive that tells the compiler to include the standard library (printf function)

int main() { // In C, the main function is the entry point of the program. There should be only 1 main inside the C project/
    printf("Hello, World!\n"); // Print a string to the console. The \n is a special character that represents a new line.
    return 0; // The return statement ends the main function and returns a value to the operating system. 0 usually indicates that the program ended successfully.
}

